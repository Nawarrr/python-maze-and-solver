def setup_maze(maze):
    for row in range(len(maze)):
        for col in range(len(maze[0])):
            char = maze[row][col]
            screen_row = 288 - (row*24)
            screen_col = -288 + (col*24)
            if char == "X":
                pen.goto(screen_col , screen_row)
                pen.stamp()
